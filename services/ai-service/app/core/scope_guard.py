def is_out_of_scope(query: str) -> Tuple[bool, str]:
    """
    检查是否明确询问其他机构（非山一大）
    
    Args:
        query: 用户查询文本
        
    Returns:
        (是否超出范围, 匹配到的机构名)
    """
    if not query:
        return False, ""
    
    # 首先检查是否包含山一大的别名（如果是山一大相关问题，直接通过）
    for alias in SDFMU_ALIASES:
        if alias in query:
            return False, ""
    
    # 检查是否包含其他机构名称
    for inst in OTHER_INSTITUTIONS:
        if inst in query:
            logger.info(f"Scope Guard 拦截: 查询包含其他机构名称 '{inst}'")
            return True, inst
    
    return False, ""


